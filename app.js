// ===================================================================
// CONFIGURATION AND IMPORTS
// ===================================================================
var express = require('express');
var app = express();

// ============================================================================
// APP CONFIG
// ============================================================================
app.use(express.static("public"));
app.set("view engine", "ejs");
// ============================================================================
// ROUTES
// ============================================================================
app.get("/", (req, res) => {
    res.send("Hello World!");
});




// ===================================================================
// SERVER SETUP
// ===================================================================
app.listen(process.env.PORT);
